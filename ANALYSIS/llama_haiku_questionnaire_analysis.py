def compute_accuracy(df, true_sources):
    """
    Computes participant-level accuracy by comparing their Source_Judgment
    for each haiku against the true sources.
    It assumes that 'Source_Judgment' columns have already been unified and cleaned
    (e.g., 'AI / Machine' to 'AI').

    Returns a new DataFrame with an 'Accuracy' column.
    """
    accuracies = []

    df_local = df.copy()

    for idx, row in df_local.iterrows():
        correct = 0
        total = 0

        for h in range(1, 13):
            col = f"Haiku{h}_Source_Judgment"

            if col not in df_local.columns:
                continue

            guess = row[col]
            truth = true_sources[h]

            # Normalize some possible label variants for comparison
            if isinstance(guess, str):
                g = guess.strip()
                if g.lower().startswith("ai"):
                    guess_norm = "AI"
                elif g.lower().startswith("human"):
                    guess_norm = "Human"
                else:
                    guess_norm = g
            else:
                guess_norm = guess

            if pd.notna(guess_norm):
                total += 1
                if guess_norm == truth:
                    correct += 1

        accuracy = correct / total if total > 0 else None
        accuracies.append(accuracy)

    df_result = df_local.copy()
    df_result["Accuracy"] = accuracies
    return df_result

