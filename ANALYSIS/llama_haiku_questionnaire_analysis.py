def build_long_pred_df(df, true_sources, dataset_name):
    """
    Long-format dataframe:
      Dataset | Respondent | Haiku | Truth | Guess | Correct (0/1)

    Uses the same cleaning logic you already have.
    """
    rows = []
    df_local = df.copy()

    REPLACEMENTS = {
        "AI / Machine": "AI",
        "AI /machine": "AI",
        "AI/machine": "AI",
        "AI/ Machine": "AI",
        "ai": "AI",
        "human": "Human",
        "Human ": "Human",
        " AI": "AI",
        "Human/AI": None,
    }

    # normalise labels
    for h in range(1, 13):
        col = f"Haiku{h}_Source_Judgment"

        if col in df_local.columns:
            df_local[col] = df_local[col].replace(REPLACEMENTS)
            df_local[col] = df_local[col].apply(
                lambda x: (
                    "AI" if isinstance(x, str) and x.strip().lower().startswith("ai")
                    else "Human" if isinstance(x, str) and x.strip().lower().startswith("human")
                    else x
                )
            )

    # build the long table
    for idx, row in df_local.iterrows():
        for h in range(1, 13):
            col = f"Haiku{h}_Source_Judgment"
            if col not in df_local.columns:
                continue

            guess = row[col]
            truth = true_sources[h]

            if pd.isna(guess):
                continue

            correct = int(guess == truth)

            rows.append({
                "Dataset": dataset_name,
                "Respondent": idx,
                "Haiku": h,
                "Truth": truth,
                "Guess": guess,
                "Correct": correct,
            })

    return pd.DataFrame(rows)

