def unify_metric(df, metric, jp_to_en=None, drop_original=True):
    """
    Unify EN + JP columns for ALL haiku 1–12 for a given metric.
    Example:
        unify_metric(df, "Source_Judgment", SOURCE_JP_TO_EN)
    will create:
        Haiku1_Source_Judgment
        Haiku2_Source_Judgment
        ...
        Haiku12_Source_Judgment
    """

    for h in range(1, 13):
        base = f"Haiku{h}_{metric}"
        col_en = f"{base}_EN"
        col_jp = f"{base}_JP"
        col_unified = base

        has_en = col_en in df.columns
        has_jp = col_jp in df.columns

        if not (has_en or has_jp):
            continue

        # Unify values
        if has_en and has_jp:
            df[col_unified] = df[col_en].combine_first(df[col_jp])
        elif has_en:
            df[col_unified] = df[col_en]
        else:
            df[col_unified] = df[col_jp]

        # Apply mapping
        if jp_to_en is not None:
            df[col_unified] = df[col_unified].replace(jp_to_en)

        # Drop original EN/JP columns
        if drop_original:
            if has_en:
                df.drop(columns=col_en, inplace=True)
            if has_jp:
                df.drop(columns=col_jp, inplace=True)

    return df

