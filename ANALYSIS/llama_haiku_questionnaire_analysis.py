    # 2) source judgment barplot (Human vs AI)
    plot_binary_source(df, haiku_num)

    # 3) theme relevance barplot (4-point agreement)
    plot_theme_agreement(df, haiku_num)

    # 4) individual barplots for each impression rating (1–5 Likert)
    for metric in LIKERT_METRICS:
        plot_likert_bar(df, haiku_num, metric)

