"""### ***Generic 1–5 Likert bar (Confidence, Fluency, …)***"""

def plot_likert_bar(df, haiku_num, metric, scale=(1, 2, 3, 4, 5)):
    col = f"Haiku{haiku_num}_{metric}"
    if col not in df.columns:
        return

    # full scale
    full_values = pd.DataFrame({col: list(scale)})

    df_count = df.groupby(col).size().reset_index(name='Count')
    df_count = full_values.merge(df_count, how='left', on=col)
    df_count['Count'] = df_count['Count'].fillna(0)

    total = df_count['Count'].sum()
    if total > 0:
        df_count['Percentage'] = (df_count['Count'] / total) * 100
    else:
        df_count['Percentage'] = 0

    fig = go.Figure(go.Bar(
        x=df_count[col],
        y=df_count['Count'],
        marker_color='steelblue'
    ))

    fig.update_layout(
        xaxis=dict(
            tickmode='array',
            tickvals=df_count[col].tolist(),
            ticktext=df_count[col].tolist(),
            title=f"{metric} value",
            showgrid=False
        ),
        yaxis_title="Count",
        showlegend=False,
        height=500,
        title=f"Haiku {haiku_num} – {metric}",
        title_x=0.5
    )

    fig.update_traces(
        hovertemplate=(
            f"{metric}: %{{x}}<br>"
            "Percentage: %{customdata[0]:.2f}%<extra></extra>"
        ),
        customdata=df_count[['Percentage']].values
    )

    fig.show()

