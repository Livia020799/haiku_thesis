# ---------------------------------------------
# Helper: normalization function
# ---------------------------------------------
def _norm_label(s: str) -> str:
    return " ".join(unicodedata.normalize("NFKC", str(s)).split())

# ---------------------------------------------
# JP → ENG background column renaming
# ---------------------------------------------
background_map_jp_raw = {
    "大学名": "University_name_JP",
    "学年（1–4）・学科（またはコース）": "Grade_department_JP",
    "日本語能力試験（JLPT）または同等の資格": "Japanese_proficiency_JP",
    "俳句の経験\n俳句を見聞きする頻度": "Haiku_experience_JP",
    "俳句の経験 俳句を見聞きする頻度": "Haiku_experience_JP",
    "俳句の作成": "Haiku_composition_JP",
}

background_map_jp = {
    _norm_label(k): v for k, v in background_map_jp_raw.items()
}

# ---------------------------------------------
# Helper: check if text contains Japanese
# ---------------------------------------------
def is_japanese(text: str) -> bool:
    return any(
        ('\u3040' <= ch <= '\u30ff') or
        ('\u4e00' <= ch <= '\u9faf')
        for ch in text
    )

# ---------------------------------------------
# Main rename function over all columns
# ---------------------------------------------
def rename_haiku_columns(cols):
    new_cols = []

    current_haiku_en = 0
    current_haiku_jp = 0

    for col in cols:
        raw = col
        # same normalization everywhere
        txt_norm = _norm_label(col)

        # 0. Background JP → EN renaming
        if txt_norm in background_map_jp:
            new_cols.append(background_map_jp[txt_norm])
            continue

        # 1. Decide language
        jp = is_japanese(txt_norm)
        metric = None
        lang = None

        if jp:
            for key, val in metric_map_jp.items():
                if key in txt_norm:
                    metric = val
                    lang = "JP"
                    break
        else:
            low = txt_norm.lower()
            for key, val in metric_map_en.items():
                if key in low:
                    metric = val
                    lang = "EN"
                    break

        # 2. If it's not a metric column → keep original name
        if metric is None or lang is None:
            new_cols.append(raw)
            continue

        # 3. Increment haiku number when Source_Judgment appears
        if lang == "EN":
            if metric == "Source_Judgment":
                current_haiku_en += 1
            haiku_num = current_haiku_en or 1
        else:
            if metric == "Source_Judgment":
                current_haiku_jp += 1
            haiku_num = current_haiku_jp or 1

        new_name = f"Haiku{haiku_num}_{metric}_{lang}"
        new_cols.append(new_name)

    return new_cols

