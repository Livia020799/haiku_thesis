def prep(df, suffix):
    d = df.copy()
    d = d.rename(columns={
        "coef": f"coef_{suffix}",
        "sd": f"sd_{suffix}",
        "odds_ratio": f"or_{suffix}"
    })
    return d[["feature", f"coef_{suffix}", f"sd_{suffix}", f"or_{suffix}"]]

