def summarize_by_set(df, set_name, metrics):
    rows = []
    for m in metrics:
        x = df.loc[df["ST_Set"] == set_name, m].dropna().to_numpy()
        n = len(x)
        mean = x.mean()
        se = x.std(ddof=1) / np.sqrt(n) if n > 1 else np.nan
        ci = 1.96 * se if n > 1 else np.nan
        rows.append((m, mean, mean-ci, mean+ci))
